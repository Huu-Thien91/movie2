        },
        changePage(newPage) {
            if (newPage >= 1 && newPage <= this.totalPages) {
                this.page = newPage; // C·∫≠p nh·∫≠t trang hi·ªán t·∫°i
                this.updatePagination(); // C·∫≠p nh·∫≠t d·ªØ li·ªáu ph√¢n trang
            }
        },
        selectTab(tab) {
            this.currentTab = tab; // Chuy·ªÉn tab
            this.searchQuery = ""; // X√≥a t·ª´ kh√≥a t√¨m ki·∫øm
            this.page = 1; // ƒê·∫∑t l·∫°i trang ƒë·∫ßu ti√™n
