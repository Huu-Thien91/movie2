                this.error = "Kh√¥ng th·ªÉ t·∫£i danh s√°ch phim!";
            } finally {
                this.loading = false;
            }
        },
        updatePagination() {
            // L·∫•y d·ªØ li·ªáu cho trang hi·ªán t·∫°i
            const startIndex = (this.page - 1) * this.itemsPerPage;
            const endIndex = startIndex + this.itemsPerPage;
