            } else {
                // T∆∞∆°ng t·ª± logic fetchSeries() n·∫øu c·∫ßn
                console.log("Fetching series...");
            }
        },
    },
    mounted() {
        this.fetchMovies(); // L·∫•y danh s√°ch phim l·∫ª khi t·∫£i trang
    },
};
</script>


