            searchQuery: "", // T·ª´ kh√≥a t√¨m ki·∫øm
            allMovies: [], // To store all movies fetched from API
            paginatedMovies: [], // To store the current page's movies
            totalPages: 0, // T·ªïng s·ªë trang
            page: 1, // Trang hi·ªán t·∫°i
